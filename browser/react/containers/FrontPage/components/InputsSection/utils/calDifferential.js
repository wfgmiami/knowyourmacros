const calDifferential = (goal) => {
  switch (goal) {
    case 'lose 4 pounds in 1 month':
      return -500;
    case -500:
      return -500;
    case 'lose 8 pounds in 1 month':
      return -1000;
    case -1000:
      return -1000;
    default:
      return 0;
  }
};

export default calDifferential;
